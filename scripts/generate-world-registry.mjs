import { readdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const worldsDir = join(root, "src", "worlds");
const outputPath = join(root, "src", "core", "world-registry.js");
const entries = await readdir(worldsDir, { withFileTypes: true });
const worlds = [];

for (const entry of entries) {
  if (!entry.isDirectory() || entry.name.startsWith("_")) continue;
  try {
    const files = await readdir(join(worldsDir, entry.name));
    if (files.includes("index.html")) worlds.push(`${entry.name}/index.html`);
  } catch {
    // Ignore incomplete world folders until they have a readable entry page.
  }
}

worlds.sort((a, b) => a.localeCompare(b));
const source = `// Generated by npm run registry. Do not edit manually.\n` +
  `globalThis.ELASTIC_SPACE_WORLDS = ${JSON.stringify(worlds, null, 2)};\n`;

await writeFile(outputPath, source, "utf8");
console.log(`Published ${worlds.length} worlds to src/core/world-registry.js`);
